package lothel.soft.ventas.model;

import java.util.Date;

/**
 *
 * @author efeproceres
 */
public class DocumentoDePago {
    private int idDocumentoPago;
    private TipoDocumento tipo;
    private Date fechaEmision;
    private Pedido pedido;

    public DocumentoDePago() {

    }

    public DocumentoDePago(TipoDocumento tipo) {
        this.tipo = tipo;
        this.fechaEmision = new Date();
    }

    public int getIdDocumentoPago() {
        return idDocumentoPago;
    }

    public void setIdDocumentoPago(int idDocumentoPago) {
        this.idDocumentoPago = idDocumentoPago;
    }

    public TipoDocumento getTipo() {
        return tipo;
    }

    public void setTipo(TipoDocumento tipo) {
        this.tipo = tipo;
    }

    public Date getFechaEmision() {
        return fechaEmision;
    }

    public void setFechaEmision(Date fechaEmision) {
        this.fechaEmision = fechaEmision;
    }

    public Pedido getPedido() {
        return pedido;
    }

    public void setPedido(Pedido pedido) {
        this.pedido = pedido;
    }
    
    
}
